[bits 16]           ; Работаем в 16-битном реальном режиме
[org 0x7c00]        ; BIOS загружает этот сектор именно по этому адресу

start:
    mov si, msg     ; Указываем на нашу строку "Welcome to KWER-OS!"
    call print      ; Вызываем функцию печати

    jmp $           ; Зацикливаемся, чтобы процессор не ушел "в разнос"

print:
    mov ah, 0x0e    ; Функция BIOS "Телетайпный вывод символа"
.loop:
    lodsb           ; Загружаем следующий байт из SI в AL
    or al, al       ; Проверяем, не дошли ли мы до конца строки (ноль)
    jz .done        ; Если ноль — выходим
    int 0x10        ; Видео-прерывание BIOS для вывода символа
    jmp .loop
.done:
    ret

msg db 'Welcome to KWER-OS!', 0

times 510-($-$$) db 0 ; Добиваем файл нулями до 510 байт
dw 0xaa55             ; Магическая сигнатура загрузочного сектора (байт 511 и 512)
